// String as primitive type ( simple type)
const s1 = 'this is a string';
console.log(s1);
console.log(typeof s1);

// String as object type ( complex type)
const s2 = new String('this is also a string');
console.log(s2.toUpperCase());
console.log(typeof s2);

// Other examples
const n1 = 11;
const n2 = new Number(11);
console.log(n1);
console.log(n2);
